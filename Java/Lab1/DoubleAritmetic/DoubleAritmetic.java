public class DoubleAritmetic
{
  public static void main(String[] args)
  {
    double a = 5.0;
    double b = -3.7;
    System.out.println("a + b = " + (a + b));
    System.out.println("a - b = " + (a - b));
    System.out.println("a * b = " + (a * b));
    System.out.println("a / b = " + (a / b));
    System.out.println("a % b = " + (a % b));
  }
}